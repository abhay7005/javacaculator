package com.util.com;

public class Utility {

	public int addition(int a, int b, int c) {
		return a + b + c;
	}
	public int substraction (int a, int b, int c) {
			return a - b - c;
	}
	public int multifiation (int a, int b, int c) {
		return a * b * c;
    }
	public int dividation (int a, int b, int c) {
		return a / b / c;
	}
}
